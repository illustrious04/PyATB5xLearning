"""
Take a 2 input from the user
Print the Quotient and Remainder
15 ->  num1
2 -> num2
Q -> 7
R -> 1
"""
# Take 2 user input
num1 = int(input("Enter the 1st number\n"))
num2 = int(input("Enter the 2ns number\n"))

# Calculate Quotient and Remainder
quotient = num1 // num2
remainder = num1 % num2

# Print the results.
print("Quotient: ", quotient)
print("Remainder: ", remainder)


























