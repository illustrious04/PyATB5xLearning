"""
Take a 3 input from the user
perform the sub, sub, mul and div
"""
# Taking user input
num1 = int(input("Enter 1st Number\n"))
num2 = int(input("Enter 2nd Number\n"))
num3 = int(input("Enter 3rd Number\n"))

# Calculation
subtraction = num1 - num2 - num3
multiplication = num1 * num2 * num3
division = num1 / num2 / num3

# Printing the output

print("The Subctraction of the numbers are: ", subtraction)
print("The multiplication of the numbers are: ", multiplication)
print("The division of the numbers are: ", division)
